#!/usr/bin/env node

/**
 * Blog Build Script
 *
 * Scans each blog post folder's post.json to generate:
 *   1. assets/js/blog/blog-data.js (post data + helper functions)
 *   2. blog/[slug].html pages from blog/_template.html (with inlined content)
 *
 * Usage: npm run build:blog
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const BLOG_DIR = path.join(ROOT, 'assets', 'content', 'blog');
const BLOG_IMAGES_DIR = path.join(ROOT, 'assets', 'images', 'blog');
const OUTPUT_JS = path.join(ROOT, 'assets', 'js', 'blog', 'blog-data.js');
const TEMPLATE_PATH = path.join(ROOT, 'blog', '_template.html');
const BLOG_OUTPUT_DIR = path.join(ROOT, 'blog');

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function readJSON(filePath) {
    return JSON.parse(fs.readFileSync(filePath, 'utf-8'));
}

/**
 * Detect all images for a blog post.
 * Follows similar pattern to gallery: cover is the "main" image,
 * img-1, img-2, etc. are additional carousel slides.
 * Light variants use -light suffix (e.g., cover-light.webp, img-1-light.webp).
 * Thumbnails are prefixed with thumb- (auto-generated by convert-to-webp.js).
 */
function detectImages(folderPath) {
    const files = fs.readdirSync(folderPath);
    const ext = '.webp';

    // All webp images excluding thumbnails
    const allImages = files.filter(f => f.endsWith(ext) && !f.startsWith('thumb-'));
    const lightFiles = new Set(allImages.filter(f => f.slice(0, -ext.length).endsWith('-light')));
    const darkFiles = allImages.filter(f => !lightFiles.has(f));

    // Cover image (main)
    const cover = darkFiles.includes(`cover${ext}`) ? `cover${ext}` : null;
    const coverLight = lightFiles.has(`cover-light${ext}`) ? `cover-light${ext}` : null;

    // Build carousel slides: cover first, then img-* sorted
    const slides = [];
    const slidesLight = [];

    if (cover) {
        slides.push(cover);
        slidesLight.push(coverLight || null);
    }

    // Additional slides (img-1.webp, img-2.webp, etc.)
    const otherImages = darkFiles
        .filter(f => f !== `cover${ext}`)
        .sort();

    for (const file of otherImages) {
        slides.push(file);
        const name = file.slice(0, -ext.length);
        const lightFile = `${name}-light${ext}`;
        slidesLight.push(lightFiles.has(lightFile) ? lightFile : null);
    }

    // Thumbnails
    const thumb = files.includes('thumb-cover.webp') ? 'thumb-cover.webp' : null;
    const thumbLight = files.includes('thumb-cover-light.webp') ? 'thumb-cover-light.webp' : null;

    const thumbSlides = slides.map(f => {
        const baseName = f.slice(0, -ext.length);
        const thumbFile = `thumb-${baseName}.webp`;
        return files.includes(thumbFile) ? thumbFile : null;
    });
    const thumbSlidesLight = slidesLight.map(f => {
        if (!f) return null;
        const baseName = f.slice(0, -ext.length);
        const thumbFile = `thumb-${baseName}.webp`;
        return files.includes(thumbFile) ? thumbFile : null;
    });

    const hasLightVariants = slidesLight.some(f => f !== null);

    return { cover, coverLight, slides, slidesLight, hasLightVariants, thumb, thumbLight, thumbSlides, thumbSlidesLight };
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function build() {
    // Discover blog post folders with post.json
    let folders;
    try {
        folders = fs.readdirSync(BLOG_DIR, { withFileTypes: true })
            .filter(d => d.isDirectory())
            .map(d => d.name)
            .filter(name => {
                if (name.startsWith('_')) return false; // Skip template/utility folders
                const postPath = path.join(BLOG_DIR, name, 'post.json');
                return fs.existsSync(postPath);
            })
            .sort();
    } catch (e) {
        console.log('No blog content directory found. Creating empty blog-data.js.');
        folders = [];
    }

    // Read all posts
    const posts = [];
    const errors = [];

    for (const folder of folders) {
        const postPath = path.join(BLOG_DIR, folder, 'post.json');
        let post;
        try {
            post = readJSON(postPath);
        } catch (e) {
            errors.push(`${folder}/post.json: Invalid JSON — ${e.message}`);
            continue;
        }

        // Validate required fields
        const required = ['id', 'title', 'slug', 'datePublished', 'category', 'excerpt'];
        const missing = required.filter(f => !post[f]);
        if (missing.length > 0) {
            errors.push(`${folder}/post.json: Missing fields: ${missing.join(', ')}`);
            continue;
        }

        // Verify slug matches folder name
        if (post.slug !== folder) {
            errors.push(`${folder}/post.json: slug "${post.slug}" does not match folder name "${folder}"`);
            continue;
        }

        // Verify content.html exists
        const contentPath = path.join(BLOG_DIR, folder, 'content.html');
        if (!fs.existsSync(contentPath)) {
            errors.push(`${folder}: Missing content.html`);
            continue;
        }

        // Detect images from assets/images/blog/{slug}/ (separate from content)
        const imagesFolderPath = path.join(BLOG_IMAGES_DIR, folder);
        post.images = fs.existsSync(imagesFolderPath)
            ? detectImages(imagesFolderPath)
            : { cover: null, coverLight: null, slides: [], slidesLight: [], hasLightVariants: false, thumb: null, thumbLight: null, thumbSlides: [], thumbSlidesLight: [] };

        posts.push(post);
    }

    if (errors.length > 0) {
        console.error('Blog build errors:');
        errors.forEach(e => console.error(`  - ${e}`));
        process.exit(1);
    }

    // Sort posts by datePublished (newest first)
    posts.sort((a, b) => new Date(b.datePublished) - new Date(a.datePublished));

    // Collect unique categories
    const categories = [...new Set(posts.map(p => p.category))].sort();

    // -----------------------------------------------------------------------
    // Generate blog-data.js
    // -----------------------------------------------------------------------

    const postsForExport = posts.map(post => ({
        id: post.id,
        title: post.title,
        slug: post.slug,
        datePublished: post.datePublished,
        category: post.category,
        excerpt: post.excerpt,
        relatedItem: post.relatedItem || null,
        coverPosition: post.coverPosition || 'center',
        images: post.images
    }));

    const js = `// AUTO-GENERATED by build/build-blog.js — DO NOT EDIT
// Source: assets/content/blog/*/post.json
// Generated: ${new Date().toISOString()}

/**
 * Blog Data Module
 * Static data for blog posts
 */

const BLOG_IMAGE_BASE = 'assets/images/blog';

// Blog Categories
export const blogCategories = ${JSON.stringify(categories, null, 2)};

// Blog Posts (sorted newest first)
export const blogPosts = ${JSON.stringify(postsForExport, null, 2)};

/**
 * Get a blog post by slug
 */
export function getPostBySlug(slug) {
    return blogPosts.find(p => p.slug === slug);
}

/**
 * Get blog posts filtered by category
 */
export function getPostsByCategory(category) {
    if (!category || category === 'all') return blogPosts;
    return blogPosts.filter(p => p.category === category);
}

/**
 * Get cover image path for a blog post (theme-aware)
 */
export function getCoverImagePath(slug, theme = 'dark', basePath = BLOG_IMAGE_BASE) {
    const post = blogPosts.find(p => p.slug === slug);
    if (!post || !post.images.cover) return null;
    if (theme === 'light' && post.images.coverLight) {
        return \`\${basePath}/\${slug}/\${post.images.coverLight}\`;
    }
    return \`\${basePath}/\${slug}/\${post.images.cover}\`;
}

/**
 * Get thumbnail cover image path for a blog post (theme-aware)
 */
export function getThumbCoverPath(slug, theme = 'dark', basePath = BLOG_IMAGE_BASE) {
    const post = blogPosts.find(p => p.slug === slug);
    if (!post) return null;
    if (theme === 'light' && post.images.thumbLight) {
        return \`\${basePath}/\${slug}/\${post.images.thumbLight}\`;
    }
    if (post.images.thumb) {
        return \`\${basePath}/\${slug}/\${post.images.thumb}\`;
    }
    // Fallback to full cover
    return getCoverImagePath(slug, theme, basePath);
}

/**
 * Get all slide image paths for a blog post (theme-aware, for carousel)
 */
export function getSlidePathsForTheme(slug, theme = 'dark', basePath = BLOG_IMAGE_BASE) {
    const post = blogPosts.find(p => p.slug === slug);
    if (!post || !post.images.slides || post.images.slides.length === 0) return [];
    return post.images.slides.map((darkFile, i) => {
        const lightFile = post.images.slidesLight[i];
        const file = (theme === 'light' && lightFile) ? lightFile : darkFile;
        return \`\${basePath}/\${slug}/\${file}\`;
    });
}

/**
 * Get all thumbnail slide paths for a blog post (theme-aware, for thumbnail strip)
 */
export function getThumbSlidePathsForTheme(slug, theme = 'dark', basePath = BLOG_IMAGE_BASE) {
    const post = blogPosts.find(p => p.slug === slug);
    if (!post || !post.images.slides || post.images.slides.length === 0) return [];
    return post.images.slides.map((darkFile, i) => {
        if (theme === 'light') {
            const lightThumb = post.images.thumbSlidesLight[i];
            if (lightThumb) return \`\${basePath}/\${slug}/\${lightThumb}\`;
        }
        const darkThumb = post.images.thumbSlides[i];
        if (darkThumb) return \`\${basePath}/\${slug}/\${darkThumb}\`;
        // Fallback to full-size slide
        const lightFile = post.images.slidesLight[i];
        const file = (theme === 'light' && lightFile) ? lightFile : darkFile;
        return \`\${basePath}/\${slug}/\${file}\`;
    });
}

/**
 * Format a date string for display
 */
export function formatDate(dateString) {
    const date = new Date(dateString + 'T00:00:00');
    return date.toLocaleDateString('en-US', {
        year: 'numeric',
        month: 'long',
        day: 'numeric'
    });
}
`;

    // Ensure output directory exists
    const outputDir = path.dirname(OUTPUT_JS);
    if (!fs.existsSync(outputDir)) {
        fs.mkdirSync(outputDir, { recursive: true });
    }

    fs.writeFileSync(OUTPUT_JS, js, 'utf-8');
    console.log(`  Generated: ${path.relative(ROOT, OUTPUT_JS)} (${posts.length} posts)`);

    // -----------------------------------------------------------------------
    // Generate blog post pages from template
    // -----------------------------------------------------------------------

    let template;
    try {
        template = fs.readFileSync(TEMPLATE_PATH, 'utf-8');
    } catch (e) {
        console.error(`Could not read template: ${TEMPLATE_PATH}`);
        process.exit(1);
    }

    let pagesGenerated = 0;
    for (const post of posts) {
        // Read the post content
        const contentPath = path.join(BLOG_DIR, post.slug, 'content.html');
        const content = fs.readFileSync(contentPath, 'utf-8');

        const ogImage = post.images.cover
            ? `https://www.sidehustle.llc/assets/images/blog/${post.slug}/${post.images.cover}`
            : 'https://www.sidehustle.llc/assets/images/og-preview.jpg';

        const html = template
            .replace(/\{\{TITLE\}\}/g, post.title)
            .replace(/\{\{DESCRIPTION\}\}/g, post.excerpt)
            .replace(/\{\{SLUG\}\}/g, post.slug)
            .replace(/\{\{OG_IMAGE\}\}/g, ogImage)
            .replace(/\{\{CONTENT\}\}/g, content)
            .replace(/\{\{CACHE_VERSION\}\}/g, Date.now());

        const outputPath = path.join(BLOG_OUTPUT_DIR, `${post.slug}.html`);
        fs.writeFileSync(outputPath, html, 'utf-8');
        pagesGenerated++;
    }

    console.log(`  Generated: ${pagesGenerated} blog pages in blog/`);

    // Summary
    console.log('');
    console.log('Blog Build Complete');
    console.log(`  Posts: ${posts.length}`);
    console.log(`  Categories: ${categories.join(', ') || '(none)'}`);
}

build();
